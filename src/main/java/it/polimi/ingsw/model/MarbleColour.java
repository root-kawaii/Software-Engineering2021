package it.polimi.ingsw.model;



/**
 * enum class for the marble colours
 */
public enum MarbleColour {
    WHITE{
        @Override
        public String toString() {
            return "WHITE";
        }
    },
    RED{
        @Override
        public String toString() {
            return "RED";
        }
    },
    YELLOW{
        @Override
        public String toString() {
            return "YELLOW";
        }
    },
    BLUE{
        @Override
        public String toString() {
            return "BLUE";
        }
    },
    GREY{
        @Override
        public String toString() {
            return "GREY";
        }
    },
    PURPLE{
        @Override
        public String toString() {
            return "PURPLE";
        }
    }





}

